# if / elif / else

condicao1 = False
condicao2 = True
condicao3 = False
condicao4 = False

if condicao1:
    print('Este é o código do if')
elif condicao2:
    print('Condição 2')
elif condicao3:
    print('Condição 3')
elif condicao4:
    print('Condição 4')
else:
    print('Nenhuma condição foi satisfeita.')

print('Fora do if')
