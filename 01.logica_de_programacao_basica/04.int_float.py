#Tipo int e float
#int -> Número inteiro
print(11) #int
print(-11) #int
print(0) #int

#float -> Número com ponto flutuante. TEM CASA DECIMAL!
print(1.1, 10.11, sep=",") #float
print(0.0, 10.11, sep=",") #float
print(0.0,-1.5 ,sep=",") #float

#A fução type mostra o tipo que o Python inferiu o valor.
print(type(1.32), type('data'), type(1))

